          <h3 class="text-2xl font-bold text-[#160D51] mb-4">Survey Findings</h3>
          <ul class="space-y-3 text-gray-700">
            <li>• Poor repairs from 2000-2002 using incorrect materials</li>
            <li>• Incorrect installation of noggins causing deck instability</li>
            <li>• Steelwork delamination due to improper edge preparation</li>
            <li>• Water traps causing pooling of salt-laden water</li>
            <li>• Severe decay affecting over 25% of decking</li>
            <li>• Inadequate surface preparation for recoating works</li>
